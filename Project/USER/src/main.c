extern uint8 into_island_flag, out_island_flag;
extern uint16 temp_right_pluse, IN_island_encoder, OUT_island_encoder, OUT_accelerate_encoder;

uint8 UI_count = 4, time_count = 0;
void main()
{
		DisableGlobalIRQ();								//关闭总中断

		clock_init(SYSTEM_CLOCK_52M);					// 初始化系统频率,勿删除此句代码。
		board_init();									// 初始化寄存器,勿删除此句代码。

    // 此处编写用户代码 例如外设初始化代码等
 
		lcd_init();							//LCD屏幕初始化
		Motor_Command_Init();				//电机初始化
		pwm_init(P33,50,0);					//舵机初始化
		Adinductance_init();				//电感初始化
		pit_timer_ms(TIM_1, 4);				//4ms执行定时器1中断
		EnableGlobalIRQ();					//开启总中断

		

    // 此处编写用户代码 例如外设初始化代码等
	
    while(1)
    {
		// 此处编写需要循环执行的代码
		if (P50 == 0) {
			UI_count++;
			lcd_clear(WHITE);
		}
		if (P51 == 0) {
			if (UI_count == 0) {
				lcd_showstr(0, 4, "ERROR");		//减小保护
			}
			else { 
				UI_count--;
				lcd_clear(WHITE);
			};
		}
		if (P16 == 0) {
			into_island_flag = 0;
			out_island_flag = 0;
			temp_right_pluse = 0;
			IN_island_encoder = 0;
			OUT_island_encoder = 0;
			OUT_accelerate_encoder = 0;
		}

		Electroma_UI();

		delay_ms(1);
    // 此处编写需要循环执行的代码
    }
}